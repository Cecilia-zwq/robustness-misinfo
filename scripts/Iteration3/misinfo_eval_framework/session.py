"""
Session Runner
==============
Orchestrates a multi-turn conversation between the SimulatedUserAgent and
the TargetLLM, scoring each turn with the Evaluator.

Public API
----------
    result = run_session(
        user_agent, target_llm, evaluator,
        misinformation_claim, n_turns=8,
    )
    # result is a SessionResult dataclass with full logs and aggregate metrics.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field

from .evaluator import Evaluator
from .target_llm import TargetLLM
from .user_agent import SimulatedUserAgent

logger = logging.getLogger(__name__)


@dataclass
class TurnRecord:
    """
    Record for a single conversational turn.
    
    Stores the complete interaction data for one cycle of:
    (1) user generates a message, (2) target LLM responds, (3) evaluator scores.
    
    Attributes
    ----------
    turn : int
        Turn number (1-indexed).
    user_message : str
        Message generated by the SimulatedUserAgent.
    target_response : str
        Response from the TargetLLM.
    scores : dict
        Evaluation scores (e.g., 'correction', 'rebuttal').
    character_breaks : int
        Number of character breaks detected by reflection on this turn.
    is_fallback : bool
        Whether the agent exhausted all retries and fell back on this turn.
    """

    turn: int
    user_message: str
    target_response: str
    scores: dict
    character_breaks: int = 0
    is_fallback: bool = False


@dataclass
class SessionResult:
    """
    Complete result of a multi-turn evaluation session.
    
    Aggregates all turns, maintains conversation history, and computes
    aggregate metrics like mean correction and rebuttal scores. Provides
    methods for analysis and summarization.
    
    Attributes
    ----------
    misinformation_claim : str
        The false claim being evaluated.
    n_turns : int
        Total number of turns in the session.
    turns : list[TurnRecord]
        Records for each conversational turn.
    conversation_history : list[dict]
        Full conversation in OpenAI chat format (role/content pairs).
    n_breaks_total : int
        Total number of character breaks flagged by the reflection module
        across all turns (including the opening).
    n_breaks_fallback : int
        Number of turns where all retries were exhausted and a fallback
        message was used.
    """

    misinformation_claim: str
    n_turns: int
    turns: list[TurnRecord] = field(default_factory=list)
    conversation_history: list[dict] = field(default_factory=list)
    n_breaks_total: int = 0
    n_breaks_fallback: int = 0

    # ════════════════════════════════════════════════════════════════════════════
    # Aggregate Metrics
    # ════════════════════════════════════════════════════════════════════════════

    @property
    def mean_correction(self) -> float:
        """Average correction score excluding fallback turns (primary DV)."""
        vals = [
            t.scores.get("correction")
            for t in self.turns
            if not t.is_fallback
            and t.scores.get("correction") is not None
            and t.scores.get("correction") != -1
        ]
        return sum(vals) / len(vals) if vals else 0.0

    @property
    def mean_rebuttal(self) -> float:
        """Average rebuttal score excluding fallback turns (primary DV)."""
        vals = [
            t.scores.get("rebuttal")
            for t in self.turns
            if not t.is_fallback
            and t.scores.get("rebuttal") is not None
            and t.scores.get("rebuttal") != -1
        ]
        return sum(vals) / len(vals) if vals else 0.0

    @property
    def mean_correction_all(self) -> float:
        """Average correction score across all turns including fallbacks."""
        vals = [
            t.scores.get("correction")
            for t in self.turns
            if t.scores.get("correction") is not None and t.scores.get("correction") != -1
        ]
        return sum(vals) / len(vals) if vals else 0.0

    @property
    def mean_rebuttal_all(self) -> float:
        """Average rebuttal score across all turns including fallbacks."""
        vals = [
            t.scores.get("rebuttal")
            for t in self.turns
            if t.scores.get("rebuttal") is not None and t.scores.get("rebuttal") != -1
        ]
        return sum(vals) / len(vals) if vals else 0.0

    @property
    def score_trajectory(self) -> list[dict]:
        """Per-turn scores (without raw_output) for easy plotting and analysis."""
        return [
            {
                "turn": t.turn,
                "correction": t.scores.get("correction"),
                "rebuttal": t.scores.get("rebuttal"),
                "character_breaks": t.character_breaks,
                "is_fallback": t.is_fallback,
            }
            for t in self.turns
        ]
    
    @property
    def character_break_rate(self) -> float:
        """Fraction of all turns where at least one character break occurred."""
        if not self.turns:
            return 0.0
        return sum(1 for t in self.turns if t.character_breaks > 0) / len(self.turns)

    def summary(self) -> dict:
        """Return a compact summary dict for logging and analysis.
        
        Returns
        -------
        dict
            Contains claim, n_turns, mean metrics, break stats, and score trajectory.
        """
        
        return {
            "misinformation_claim": self.misinformation_claim,
            "n_turns": self.n_turns,
            "mean_correction": round(self.mean_correction, 3),
            "mean_rebuttal": round(self.mean_rebuttal, 3),
            "mean_correction_all": round(self.mean_correction_all, 3),
            "mean_rebuttal_all": round(self.mean_rebuttal_all, 3),
            "n_breaks_total": self.n_breaks_total,
            "n_breaks_fallback": self.n_breaks_fallback,
            "character_break_rate": round(self.character_break_rate, 3),
            "score_trajectory": self.score_trajectory,
        }


def run_session(
    user_agent: SimulatedUserAgent,
    target_llm: TargetLLM,
    evaluator: Evaluator,
    misinformation_claim: str,
    n_turns: int = 8,
) -> SessionResult:
    """
    Run a complete multi-turn evaluation session.
    
    Orchestrates a conversation where:
    1. The SimulatedUserAgent advocates for a misinformation claim.
    2. The TargetLLM responds to defend against the false claim.
    3. The Evaluator scores the TargetLLM's response for quality of
       correction and rebuttal.
    
    This process repeats for n_turns, building a conversation history and
    collecting scores for analysis.

    Parameters
    ----------
    user_agent : SimulatedUserAgent
        The adversarial simulated user that advocates for the false claim.
    target_llm : TargetLLM
        The model under evaluation. Should resist the misinformation.
    evaluator : Evaluator
        Scores each target-LLM response for correction and rebuttal quality.
    misinformation_claim : str
        The false claim the simulated user advocates for throughout the session.
    n_turns : int, optional
        Number of conversational turns (default: 8).
        Each turn = (user message → LLM response → evaluation).

    Returns
    -------
    SessionResult
        Complete session result with:
        - All turn records (messages, responses, scores, break info).
        - Full conversation history.
        - Aggregate metrics (mean correction, mean rebuttal, break stats).
    """
    result = SessionResult(
        misinformation_claim=misinformation_claim,
        n_turns=n_turns,
    )
    conversation_history: list[dict] = []

    # ════════════════════════════════════════════════════════════════════════════
    # Main Loop: Conduct Multi-Turn Conversation
    # ════════════════════════════════════════════════════════════════════════════
    
    for turn_idx in range(1, n_turns + 1):
        logger.info("──── Turn %d / %d ────", turn_idx, n_turns)

        # ─ Step 1: Simulated User generates a message ──────────────────────────────
        # On the first turn, generate an opening statement of the false claim.
        # On subsequent turns, generate a contextual reply based on conversation history.
        if turn_idx == 1:
            user_msg = user_agent.generate_opening(misinformation_claim)
        else:
            user_msg = user_agent.generate_reply(
                conversation_history, misinformation_claim
            )
        
        # Read per-call break stats from the agent
        breaks = user_agent._last_break_count
        is_fallback = user_agent._last_fallback

        # Accumulate session-level break counters
        result.n_breaks_total += breaks
        if is_fallback:
            result.n_breaks_fallback += 1

        conversation_history.append({"role": "user", "content": user_msg})
        logger.info("USER: %s", user_msg)

        # ─ Step 2: Target LLM responds ─────────────────────────────────────────────
        # The model under evaluation receives the full conversation history
        # and generates a response to counter / correct the misinformation.
        target_response = target_llm.respond(conversation_history)
        conversation_history.append({"role": "assistant", "content": target_response})
        logger.info("TARGET: %s", target_response[:240])

        # ─ Step 3: Evaluator scores the response ───────────────────────────────────
        # Score the LLM's response on:
        # - correction: effectiveness at correcting the false claim.
        # - rebuttal: quality of the counterargument.
        scores = evaluator.evaluate(
            user_message=user_msg,
            response=target_response,
            misinformation_claim=misinformation_claim,
        )
        logger.info(
            "SCORES — correction: %.0f, rebuttal: %.0f | "
            "character_breaks: %d, is_fallback: %s",
            scores.get("correction", -1),
            scores.get("rebuttal", -1),
            breaks,
            is_fallback,
        )

        # ─ Record this turn ────────────────────────────────────────────────────────
        result.turns.append(
            TurnRecord(
                turn=turn_idx,
                user_message=user_msg,
                target_response=target_response,
                scores=scores,
                character_breaks=breaks,
                is_fallback=is_fallback,
            )
        )

    # ════════════════════════════════════════════════════════════════════════════
    # Finalize and Return Results
    # ════════════════════════════════════════════════════════════════════════════
    
    result.conversation_history = conversation_history
    return result